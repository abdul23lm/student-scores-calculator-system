    public final void viewDataTabel(){
        String [] kolom ={"NIM","Nama","Alamat","Mata Kuliah","Nilai Akhir"};
        Object [][] objectmahasiswa = new Object[datamhs.getALL().size()][5];
        int i=0;
        for(DataMahasiswa m: datamhs.getALL()){
            String arrayMahasiswa[]={
                m.getNIM(),
                m.getNama(),
                m.getAlamat(),
                m.getMataKuliah(),
                m.getNilaiAkhir()
            };
            objectmahasiswa[i] = arrayMahasiswa;
            i++;
        }
        modelmhs = new DefaultTableModel(objectmahasiswa, kolom);
        jTableData.setModel(modelmhs);
    }
    
