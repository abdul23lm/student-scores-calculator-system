    private void simpanBTActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_simpanBTActionPerformed
        //Simpan
        int a = Integer.parseInt(nilai1TF.getText());
        int b = Integer.parseInt(nilai2TF.getText());
        int c = Integer.parseInt(nilaikuisTF.getText());
        int d = Integer.parseInt(niliaiutsTF.getText());
        int e = Integer.parseInt(nilaiuasTF.getText());
        double f = (a * 0.1) + (b * 0.15) + (c * 0.25) + (d * 0.15) + (e * 0.35);
        nilaiakhirTF.setText(f+"");
        
        datamhs.insertDataMHS(
            nimTF.getText(),
            namaTF.getText(),
            alamatTF.getText(),
            matakuliahTF.getText(),
           
            
            nilaiakhirTF.getText()
        );
        viewDataTabel();
        Clear();
    }//GEN-LAST:event_simpanBTActionPerformed

